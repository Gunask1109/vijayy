          <div class="col-mt-4 col-md-6">
             <p>Sivakarthikeyan is an Indian film actor, producer, playback singer and lyricist who is well known for for his work in Tamil films. He was born on 17 February 1985 in Trichy, Tamil Nadu, India. He finished her schooling from Campion Anglo Indian Sr. Sec. School and engineering from J.J. College of Engineering and Technology, Trichy, Tamil Nadu, India. Then he moved to Chennai at Institute of Planning and Management for his degree of MBA. On 27th August 2010, he had an arranged marriage with Aarthi and the ceremony was conducted by their families. The couple has one daughter name Aaradhana who was born on 22 October 2013.</p>
              <p>Sivakarthikeyan began his acting career through television as a comedian and reality show personality at Star Vijay. When he was in college he performed several mimicry and comedy role on stage. During his studies, he took three months break to go for the auditions of a reality show Kalakka Povathu Yaaru and he won the show. Then he appeared in few short films like Mugapputhagam, Kurahl 786 and 360 and Identity etc. Sivakarthikeyan made his lead role debut by the film Marina which was directed by Pandiraj. His performance was mind blowing and gave a major success at the box office.</p>
              <p>Later he worked in several hit films like Manam Kothi Paravai (2012), Ethir Neechal (2013), Maan Karate (2014), Rajni Murugan (2015), Remo (2016), Velaikkaran (2017), Seema Raja (2018), Kanaa (2018) etc. In addition of films, Sivakarthikeyan also worked in several television shows such as Kalakkal Champions (2007), Jodi Number One: season 3 (2007), Kana Kaanum Kaalangal: season 1 (2008), Boys vs Girls: season 1 (2008), Adhu Idhu Edhu (2009), Super Singer Junior (2009), Airtel Super Singer (2010), Koffee with Siva (2010), 5th Vijay Awards (2011), 6th Vijay Awards (2012) and Comedy Super Night 2 (2016).</p>
