<p>Vijay is a famous Indian superstar. His birth name is Joseph Vijay Chandrasekhar. He is popular for his actor, playback singer, philanthropist and producer profile in Tamil movie industry. His fans call him Ilayathalapathy (pet name). Mr. S.A.Chandrasekharan is the name of his father who was a well-known movie director, writer and producer. Mrs. Shobha Chandrasekharan is his mother who was singer, story writer and director. He born in this family on 22 June 1974. After completion of his school education, he joined Visual Communications at Loyola College, Chennai for further studies. On 25 August 1999, he got married with Sangeetha Sornalingam, the couple has two children Sanjay (boy) and Divya (girl).</p>


 
<p>He started appearing in movies since childhood from movie Vetri as a child artist. But he made his official debut in the year 1992 movie Naalaya Theerpu produced by his father. Vijay has won several awards for his work in Tami movies. He is also running a social service organization named as Vijay Makkal Iyakkam. It was launched on 26 July 2009 at Pudukottai.</p>
              
